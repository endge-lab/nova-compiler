import fs from 'node:fs'
import path from 'node:path'
import type { Plugin } from 'vite'
import { baseParse, NodeTypes } from '@vue/compiler-dom'
import { parse as parseVueSfc } from '@vue/compiler-sfc'
import { compileNovaCss, generateNovaCssModule } from '@/css/NovaCssCompiler'
import { compileNovaSfc } from '@/sfc/NovaSfcCompiler'

export interface NovaGeneratedOutputOptions {
  enabled?: boolean
  dir?: string
}

export interface NovaVitePluginOptions {
  includeDiagnostics?: boolean
  generatedOutput?: NovaGeneratedOutputOptions
}

interface VirtualNovaModule {
  source: string
  filename: string
  ownerFile: string
}

interface ParsedAttr {
  name: string
  value: string | true
}

interface VueNovaStyleTransform {
  source: string
  styles: Array<string>
}

interface Replacement {
  start: number
  end: number
  value: string
}

const NOVA_GEN_DEBUG_ENV = 'NOVA_GEN_DEBUG_ENABLED'
const DEFAULT_GENERATED_OUTPUT_DIR = '.nova-generated'
const GENERATED_OUTPUT_HEADER = '// Generated by @endge/nova-compiler. Do not edit.\n\n'
const NOVA_STYLE_LANGS = new Set(['novacss', 'novascc'])

/** Vite plugin для `.nova`, `.novacss` и Vue `#nova-template`. */
export function novaVitePlugin(options: NovaVitePluginOptions = {}): Plugin {
  const generatedOutput = resolveGeneratedOutputOptions(options.generatedOutput)
  const virtualModules = new Map<string, VirtualNovaModule>()

  return {
    name: 'endge-nova-compiler',
    enforce: 'pre',

    resolveId(id) {
      if (virtualModules.has(stripViteQuery(id))) return id
      return null
    },

    load(id) {
      const virtualModule = virtualModules.get(stripViteQuery(id))
      if (!virtualModule) return null
      return compileNovaModule(virtualModule.source, virtualModule.filename, this, options, generatedOutput)
    },

    transform(source, id) {
      const cleanId = stripViteQuery(id)

      if (virtualModules.has(id)) {
        return null
      }

      if (cleanId.endsWith('.novacss')) {
        const result = compileNovaCss(source, {
          filename: cleanId,
          resolveImport: (request, from) => resolveSourceImport(request, from, this),
        })
        const code = generateNovaCssModule(result)
        emitDiagnostics(cleanId, result.diagnostics, options.includeDiagnostics ?? true)
        throwOnErrors(result.diagnostics)
        writeGeneratedOutput(generatedOutput, cleanId, code)
        return {
          code,
          map: null,
        }
      }

      if (cleanId.endsWith('.nova')) {
        return {
          code: compileNovaModule(source, cleanId, this, options, generatedOutput),
          map: null,
        }
      }

      if (cleanId.endsWith('.vue')) {
        clearVueInlineVirtualModules(virtualModules, cleanId)
        const novaStyles = extractVueNovaStyles(source)
        const transformed = transformVueNovaTemplateSlots(novaStyles.source, cleanId, virtualModules, options.includeDiagnostics ?? true, novaStyles.styles)
        if (transformed) {
          writeGeneratedOutput(generatedOutput, `${cleanId}__nova_template_transform`, transformed)
          return {
            code: transformed,
            map: null,
          }
        }
      }

      return null
    },
  }
}

function compileNovaModule(
  source: string,
  id: string,
  context: { addWatchFile: (id: string) => void },
  options: NovaVitePluginOptions,
  generatedOutput: Required<NovaGeneratedOutputOptions>,
): string {
  const result = compileNovaSfc(source, {
    filename: id,
    resolveImport: (request, from) => resolveSourceImport(request, from, context),
  })
  emitDiagnostics(id, result.diagnostics, options.includeDiagnostics ?? true)
  throwOnErrors(result.diagnostics)
  writeGeneratedOutput(generatedOutput, id, result.code)
  return result.code
}

function transformVueNovaTemplateSlots(
  source: string,
  id: string,
  virtualModules: Map<string, VirtualNovaModule>,
  includeDiagnostics: boolean,
  novaStyleBlocks: Array<string> = [],
): string | null {
  const imports: Array<string> = []
  const replacements: Array<Replacement> = []
  let index = 0

  const sfc = parseVueSfc(source, { filename: id })
  const template = sfc.descriptor.template
  if (!template) return null

  const ast = baseParse(template.content)
  const templateOffset = template.loc.start.offset

  walkVueTemplateElements(ast, node => {
    if (node.tag !== 'template' || !hasNovaTemplateSlot(node) || isVueSlotRelay(node)) return

    const componentName = `__NovaTemplate${index}`
    const debugId = `${stripViteQuery(id)}__nova_template_${index}`
    const nodeSource = node.loc.source
    const openingEndInNode = nodeSource.indexOf('>') + 1
    const openingSource = openingEndInNode > 0 ? nodeSource.slice(0, openingEndInNode) : nodeSource
    const attrs = parseAttrs(openingSource)
    const src = readParsedAttr(attrs, 'src')
    const innerSource = readNodeInnerSource(nodeSource)
    let virtualVersionSource = innerSource.trim()
    let importSource: string

    if (src) {
      importSource = src
      if (innerSource.trim()) {
        emitDiagnostics(id, [{
          severity: 'warning',
          code: 'nova-template-src-ignores-children',
          message: '<template #nova-template src="..."> игнорирует внутреннее содержимое.',
        }], includeDiagnostics)
      }
    } else {
      importSource = `virtual:nova-template:${stripViteQuery(id)}:${index}.nova`
      const inlineSource = createInlineNovaTemplateSource(innerSource.trim(), novaStyleBlocks)
      virtualVersionSource = inlineSource
      virtualModules.set(importSource, {
        filename: `${stripViteQuery(id)}__nova_template_${index}.nova`,
        source: inlineSource,
        ownerFile: stripViteQuery(id),
      })
    }

    const importModuleSource = src ? importSource : withVirtualVersion(importSource, virtualVersionSource)
    imports.push(`import ${componentName} from ${JSON.stringify(importModuleSource)};`)

    const forwardedAttrs = attrs
      .filter(attr => attr.name !== 'src' && !isNovaTemplateSlotAttr(attr.name))
      .map(serializeVueAttr)
      .filter(Boolean)
    const marker = `<nova-template :component="${componentName}" source="${escapeAttr(src ?? importSource)}" debug-id="${escapeAttr(debugId)}"${forwardedAttrs.length ? ` ${forwardedAttrs.join(' ')}` : ''} />`

    replacements.push({
      start: templateOffset + node.loc.start.offset,
      end: templateOffset + node.loc.end.offset,
      value: `<template #nova-template>${marker}</template>`,
    })
    index += 1
  })

  if (replacements.length === 0) return null
  return injectVueScriptSetupImports(applyReplacements(source, replacements), imports.join('\n'))
}

function extractVueNovaStyles(source: string): VueNovaStyleTransform {
  const styles: Array<string> = []
  const transformed = source.replace(/<style\b([^>]*)>([\s\S]*?)<\/style>/gi, (raw: string, attrsSource: string) => {
    const attrs = parseAttrs(attrsSource)
    const lang = readParsedAttr(attrs, 'lang')?.toLowerCase()
    if (!lang || !NOVA_STYLE_LANGS.has(lang)) return raw

    styles.push(raw)
    return ''
  })

  return {
    source: transformed,
    styles,
  }
}

function createInlineNovaTemplateSource(templateSource: string, novaStyleBlocks: Array<string>): string {
  const blocks = [`<template>\n${templateSource}\n</template>`]
  if (novaStyleBlocks.length) blocks.push(...novaStyleBlocks)
  return blocks.join('\n\n')
}

function walkVueTemplateElements(node: any, visit: (node: any) => void): void {
  if (node.type === NodeTypes.ELEMENT) visit(node)
  if (!Array.isArray(node.children)) return
  for (const child of node.children) walkVueTemplateElements(child, visit)
}

function hasNovaTemplateSlot(node: any): boolean {
  return node.props?.some((prop: any) => prop.type === NodeTypes.DIRECTIVE
    && prop.name === 'slot'
    && prop.arg?.content === 'nova-template')
}

function isVueSlotRelay(node: any): boolean {
  return node.children?.some((child: any) => child.type === NodeTypes.ELEMENT && child.tag === 'slot') ?? false
}

function isNovaTemplateSlotAttr(name: string): boolean {
  return name === '#nova-template' || name === 'v-slot:nova-template'
}

function readNodeInnerSource(nodeSource: string): string {
  const openingEnd = nodeSource.indexOf('>')
  if (openingEnd < 0) return ''
  if (nodeSource.slice(0, openingEnd).trimEnd().endsWith('/')) return ''

  const closingStart = nodeSource.lastIndexOf('</template>')
  if (closingStart < 0) return ''
  return nodeSource.slice(openingEnd + 1, closingStart)
}

function applyReplacements(source: string, replacements: Array<Replacement>): string {
  let transformed = source
  const ordered = [...replacements].sort((a, b) => b.start - a.start)

  for (const replacement of ordered) {
    transformed = `${transformed.slice(0, replacement.start)}${replacement.value}${transformed.slice(replacement.end)}`
  }

  return transformed
}

function clearVueInlineVirtualModules(virtualModules: Map<string, VirtualNovaModule>, ownerFile: string): void {
  for (const [id, module] of virtualModules.entries()) {
    if (module.ownerFile === ownerFile) virtualModules.delete(id)
  }
}

function withVirtualVersion(importSource: string, source: string): string {
  return `${importSource}?v=${hashString(source)}`
}

function parseAttrs(source: string): Array<ParsedAttr> {
  const attrs: Array<ParsedAttr> = []
  const pattern = /([#:@.\w-]+)(?:=(?:"([^"]*)"|'([^']*)'))?/g
  let match: RegExpExecArray | null

  while ((match = pattern.exec(source)) !== null) {
    attrs.push({
      name: match[1],
      value: match[2] ?? match[3] ?? true,
    })
  }

  return attrs
}

function readParsedAttr(attrs: ReadonlyArray<ParsedAttr>, name: string): string | undefined {
  const attr = attrs.find(item => item.name === name)
  return typeof attr?.value === 'string' ? attr.value : undefined
}

function serializeVueAttr(attr: ParsedAttr): string {
  if (attr.value === true) return attr.name
  return `${attr.name}="${escapeAttr(attr.value)}"`
}

function injectVueScriptSetupImports(source: string, imports: string): string {
  const setupPattern = /<script\b([^>]*)\bsetup\b([^>]*)>/i
  const match = setupPattern.exec(source)
  if (!match) {
    return `<script setup>\n${imports}\n</script>\n${source}`
  }

  const insertAt = match.index + match[0].length
  return `${source.slice(0, insertAt)}\n${imports}${source.slice(insertAt)}`
}

function resolveGeneratedOutputOptions(options: NovaGeneratedOutputOptions = {}): Required<NovaGeneratedOutputOptions> {
  return {
    enabled: options.enabled ?? process.env[NOVA_GEN_DEBUG_ENV] === 'true',
    dir: options.dir ?? DEFAULT_GENERATED_OUTPUT_DIR,
  }
}

function resolveSourceImport(
  request: string,
  from: string | undefined,
  context: { addWatchFile: (id: string) => void },
): { source: string; filename: string } | null {
  const baseDir = from ? path.dirname(stripViteQuery(from)) : process.cwd()
  const resolved = request.startsWith('@/') || request === '@'
    ? path.resolve(process.cwd(), 'src', request.slice(2))
    : path.resolve(baseDir, request)
  if (!fs.existsSync(resolved)) return null
  context.addWatchFile(resolved)
  return {
    source: fs.readFileSync(resolved, 'utf8'),
    filename: resolved,
  }
}

function writeGeneratedOutput(
  generatedOutput: Required<NovaGeneratedOutputOptions>,
  sourceId: string,
  code: string,
): void {
  if (!generatedOutput.enabled) return

  const outputPath = resolveGeneratedOutputPath(generatedOutput.dir, sourceId)
  fs.mkdirSync(path.dirname(outputPath), { recursive: true })
  fs.writeFileSync(outputPath, `${GENERATED_OUTPUT_HEADER}${code}`, 'utf8')
}

function resolveGeneratedOutputPath(outputDir: string, sourceId: string): string {
  const root = process.cwd()
  const sourcePath = stripViteQuery(sourceId)
  const normalizedSourcePath = path.normalize(sourcePath)
  let relativePath = path.isAbsolute(normalizedSourcePath)
    ? path.relative(root, normalizedSourcePath)
    : normalizedSourcePath

  if (relativePath.startsWith('..') || path.isAbsolute(relativePath)) {
    relativePath = path.join('__external__', normalizedSourcePath.replace(/^[A-Za-z]:/, '').replace(/^[/\\]+/, ''))
  }

  const safeRelativePath = relativePath
    .split(path.sep)
    .map(segment => segment.replace(/[<>:"|?*]/g, '_'))
    .join(path.sep)

  return path.resolve(root, outputDir, `${safeRelativePath}.ts`)
}

function stripViteQuery(id: string): string {
  return id.split('?')[0]
}

function hashString(value: string): string {
  let hash = 2166136261

  for (let index = 0; index < value.length; index += 1) {
    hash ^= value.charCodeAt(index)
    hash = Math.imul(hash, 16777619)
  }

  return (hash >>> 0).toString(36)
}

function escapeAttr(value: string): string {
  return value.replace(/&/g, '&amp;').replace(/"/g, '&quot;')
}

function throwOnErrors(
  diagnostics: Array<{ severity: string; code: string; message: string; line?: number; column?: number }>,
): void {
  const error = diagnostics.find(item => item.severity === 'error')
  if (!error) return
  throw new Error(`${error.code}: ${error.message}`)
}

function emitDiagnostics(
  id: string,
  diagnostics: Array<{ severity: string; code: string; message: string; line?: number; column?: number }>,
  enabled: boolean,
): void {
  if (!enabled) return
  for (const diagnostic of diagnostics) {
    const location = diagnostic.line ? `${diagnostic.line}:${diagnostic.column ?? 1}` : '0:0'
    const message = `[${id}:${location}] ${diagnostic.code}: ${diagnostic.message}`
    if (diagnostic.severity === 'error') {
      console.error(message)
    } else {
      console.warn(message)
    }
  }
}
